package pojo;

//航班价格
public class Fprice {
	private String fno;//航班号
	private String flightType;//航班类型，坐的是什么舱
	private String price;//价格
	
	public String getFno() {
		return fno;
	}
	public String getFlightType() {
		return flightType;
	}
	public String getPrice() {
		return price;
	}
	
	
}
