package pojo;

public class Airplane {
	private String pno;//飞机编号
	private String cno;//所属公司编号
	private String ptype;//飞机型号
	private String capcity;//总容量
	private String ecapcity;//经济舱容量
	private String bcapcity;//商务舱容量
	private String fcapcity;//头等舱容量
	public String getPno() {
		return pno;
	}
	public String getCno() {
		return cno;
	}
	public String getPtype() {
		return ptype;
	}
	public String getCapcity() {
		return capcity;
	}
	public String getEcapcity() {
		return ecapcity;
	}
	public String getBcapcity() {
		return bcapcity;
	}
	public String getFcapcity() {
		return fcapcity;
	}
	
	
}
